    def get_active_call_id_by_cospace_id(self,cospace_id):
        response = self.cms_get(f"calls")
        root = ET.fromstring(response.text)
        for call in root.findall("call"):
            call_id = call.get('id')

            call_detail = self.cms_get(f"calls/{call_id}")
            if call_detail.status_code == 200:
                call_root = ET.fromstring(call_detail.text)
                if call_root.findtext("coSpace") == cospace_id:
                    return call_id
        return None                

     
    #### returns the leg_id of a specific participants who was found on a call
    def get_participant_leg_id(self,call_id,participant_name):
        response = self.cms_get(f"calls/{call_id}/callLegs")
        if response.status_code == 200:
            root = ET.fromstring(response.text)
            legs = root.findall("callLeg")
            for leg in legs:
                leg_id = leg.get('id')
                #print(leg_id)
                leg_detail = self.cms_get(f"callLegs/{leg_id}")
                if leg_detail.status_code == 200:
                    leg_root= ET.fromstring(leg_detail.text)
                    name_in_cms = leg_root.findtext("remoteParty")
                    #print(name_in_cms)
                    if name_in_cms and participant_name.lower() in name_in_cms.lower():
                        return leg_id
    

