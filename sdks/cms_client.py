    def etree_to_dict(self,tree):
        # print(tree)
        d = {}
        if tree.tag is None:
            return tree.text
        if tree.attrib:
            if 'total' in tree.attrib:
                d['total meetings'] = tree.attrib
            if 'id' in tree.attrib:
                d['attributes'] = tree.attrib
        children = tree.getchildren()
        if children:
            # print(children)
            for child in children:
                # print(child.tag)
                child_dict = self.etree_to_dict(child)
                if child.tag in d:
                    # print(child.tag)
                    if type(d[child.tag]) == list:
                        d[child.tag].append(child_dict)
                    else:
                        d[child.tag] = [d[child.tag],child_dict]
                else:
                    d[child.tag] = child_dict
        else:
            d[tree.tag] = tree.text
        return d

