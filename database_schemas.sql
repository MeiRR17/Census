-- =====================================================
-- Triggers for Updated Timestamps
-- =====================================================

CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Apply triggers to all tables
CREATE TRIGGER update_cucm_phones_updated_at BEFORE UPDATE ON cucm_phones 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_cucm_lines_updated_at BEFORE UPDATE ON cucm_lines 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_cucm_users_updated_at BEFORE UPDATE ON cucm_users 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_cms_cospaces_updated_at BEFORE UPDATE ON cms_cospaces 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_cms_calls_updated_at BEFORE UPDATE ON cms_calls 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

CREATE TRIGGER update_cms_call_participants_updated_at BEFORE UPDATE ON cms_call_participants 
    FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

