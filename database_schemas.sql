-- CUCM Active Phones View
CREATE OR REPLACE VIEW cucm_active_phones AS
SELECT 
    p.uuid,
    p.name,
    p.description,
    p.product_name,
    p.model,
    p.protocol,
    p.ip_address,
    p.mac_address,
    p.device_pool,
    p.location,
    p.status,
    p.created_at,
    p.updated_at
FROM cucm_phones p
WHERE p.status = 'registered';

-- CUCM Users with Devices View
CREATE OR REPLACE VIEW cucm_users_with_devices AS
SELECT 
    u.user_id,
    u.first_name,
    u.last_name,
    u.display_name,
    u.mail_id,
    u.telephone_number,
    u.department,
    u.status,
    COUNT(DISTINCT p.uuid) as device_count,
    STRING_AGG(DISTINCT p.name, ', ') as device_names
FROM cucm_users u
LEFT JOIN cucm_phones p ON p.uuid = ANY(u.associated_devices)
GROUP BY u.user_id, u.first_name, u.last_name, u.display_name, u.mail_id, u.telephone_number, u.department, u.status;

-- CMS Active Calls View
CREATE OR REPLACE VIEW cms_active_calls AS
SELECT 
    c.call_id,
    c.cospace_id,
    c.cospace_name,
    c.cospace_uri,
    c.call_type,
    c.call_state,
    c.start_time,
    c.end_time,
    c.duration,
    c.host_user_name,
    c.current_participants,
    c.max_participants,
    c.status,
    c.created_at,
    c.updated_at
FROM cms_calls c
WHERE c.call_state = 'ACTIVE';

-- CMS Call Participants View
CREATE OR REPLACE VIEW cms_call_participants_details AS
SELECT 
    cp.participant_id,
    cp.call_id,
    cp.cospace_id,
    cp.participant_name,
    cp.participant_email,
    cp.participant_role,
    cp.participant_status,
    cp.join_time,
    cp.leave_time,
    cp.duration,
    cp.is_host,
    cp.is_moderator,
    cp.is_presenter,
    cp.is_muted,
    cp.is_video_muted,
    cp.is_content_sharing,
    cp.status,
    cp.created_at,
    cp.updated_at,
    c.cospace_name,
    c.host_user_name
FROM cms_call_participants cp
LEFT JOIN cms_calls c ON cp.call_id = c.call_id;

